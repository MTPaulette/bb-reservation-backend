<?php

namespace App\Policies;

use App\Models\User;
use Illuminate\Auth\Access\HandlesAuthorization;

class UserPolicy
{
    use HandlesAuthorization;

    /**
     * Determine whether the user can view any models.
     *
     * @param  \App\Models\User  $user
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function viewAny(User $user)
    {
        if(
            $user->hasPermission("show_all_client") ||
            $user->hasPermission("show_all_staff") ||
            $user->hasPermission("show_all_staff_of_agency") ||
            $user->hasPermission("show_all_superadmin")
        ) {
            return true;
        }
        return false;
    }

    /**
     * Determine whether the user can view the model.
     *
     * @param  \App\Models\User  $user == authenticated user
     * @param  \App\Models\User  $model
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function view(User $user, User $model)
    {
        if(
            $user->hasPermission("view_client") ||
            $user->hasPermission("view_staff") ||
            $user->hasPermission("view_staff_of_agency") ||
            $user->hasPermission("view_superadmin") ||
            $user->id == $model->id
        ) {
            return true;
        }
        return false;
    }

    /**
     * Determine whether the user can create models.
     *
     * @param  \App\Models\User  $user
     * @return \Illuminate\Auth\Access\Response|bool
     */
